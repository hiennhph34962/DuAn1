        //lay thong tin cua dong dang chon => hien thi len form
        txtMaVoucher.setText(tblVoucher.getValueAt(index, 1).toString());
        txtTenVoucher.setText(tblVoucher.getValueAt(index, 2).toString());
        txtNgayTao.setText(tblVoucher.getValueAt(index, 3).toString());
        txtMaNV.setText(tblVoucher.getValueAt(index, 4).toString());
        txtNgayBatDau.setText(tblVoucher.getValueAt(index, 5).toString());
        txtNgayHetHan.setText(tblVoucher.getValueAt(index, 6).toString());
        txtSoLuong.setText(tblVoucher.getValueAt(index, 7).toString());
        txtGiaTri.setText(tblVoucher.getValueAt(index, 9).toString());
