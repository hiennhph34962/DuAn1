    private Voucher getVoucherFrom() {
        Voucher vc = new Voucher();
        vc.setMaVoucher(txtMaVoucher.getText());
        vc.setTenVc(txtTenVoucher.getText());
        vc.setNgayTao(convertStringToSqlDate(txtNgayTao.getText()));
        vc.setMaNV(txtMaNV.getText());
        vc.setNgayBatDau(convertStringToSqlDate(txtNgayBatDau.getText()));
        vc.setNgayHetHan(convertStringToSqlDate(txtNgayHetHan.getText()));
        vc.setSoLuong(Integer.parseInt(txtSoLuong.getText()));
        vc.setKieuGiam(rdoPhanTram.isSelected());
        vc.setGiaTri(Double.parseDouble(txtGiaTri.getText()));
        vc.setTrangThai(rdoCon.isSelected());
        return vc;
    }

    private java.sql.Date convertStringToSqlDate(String dateStr) {
        SimpleDateFormat formatter = new SimpleDateFormat("yyyy-MM-dd"); // Định dạng ngày sinh của bạn
        try {
            Date utilDate = formatter.parse(dateStr); // Chuyển đổi thành java.util.Date
            return new java.sql.Date(utilDate.getTime()); // Chuyển đổi thành java.sql.Date
        } catch (ParseException e) {
            e.printStackTrace();
            return null; // hoặc có thể xử lý ngoại lệ theo cách khác
        }
    }

