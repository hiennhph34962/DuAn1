    private void LoadDataToTable() {
        model.setRowCount(0);
        //goi phuong thuc getAllSinhVien() cua SinhVienService  de lay du lieu
        int stt = 1;
        ArrayList<NhanVien> lst = nvsv.getAllNhanVien();
        for (NhanVien sv : lst) {
            model.addRow(new Object[]{
                stt++,
                sv.getMaNv(),
                sv.getPass(),
                sv.getTen(),
                sv.getNgaySinh(),
                sv.getSdt(),
                sv.getEmail(),
                sv.isChucVu() ? "Quản lý" : "Nhân Viên",
                sv.getLuong(),
                sv.getDiaChi(),
                sv.isTrangThai() ? "Còn làm việc" : "Ngưng làm việc"
            });
        }
    }

    private void LoadDataToTable(String ma) {
        model.setRowCount(0);
        //goi phuong thuc getAllSinhVien() cua SinhVienService  de lay du lieu
        int stt = 1;
        NhanVien sv = nvsv.timKiemNhanVienTheoMa(ma);

        model.addRow(new Object[]{
            stt++,
            sv.getMaNv(),
            sv.getPass(),
            sv.getTen(),
            sv.getNgaySinh(),
            sv.getSdt(),
            sv.getEmail(),
            sv.isChucVu() ? "Quản lý" : "Nhân Viên",
            sv.getLuong(),
            sv.getDiaChi(),
            sv.isTrangThai() ? "Còn làm việc" : "Ngưng làm việc"
        });

    }

    private void showDetails() {
        //Lay Dong dang chon tren table
        int index = tblNhanVien.getSelectedRow();
        //lay thong tin cua dong dang chon => hien thi len form
        txtMaNV.setText(tblNhanVien.getValueAt(index, 1).toString());
        txtMatKhau.setText(tblNhanVien.getValueAt(index, 2).toString());
        txtTenNv.setText(tblNhanVien.getValueAt(index, 3).toString());
        txtNgaySinh.setText(tblNhanVien.getValueAt(index, 4).toString());
        txtSdt.setText(tblNhanVien.getValueAt(index, 5).toString());
        txtEmail.setText(tblNhanVien.getValueAt(index, 6).toString());
        boolean chucVu = tblNhanVien.getValueAt(index, 7).toString().equalsIgnoreCase("Quản lý") ? true : false;
        rdoQuanLy.setSelected(chucVu);
        rdoNhanVien.setSelected(!chucVu);
        txtLuong.setText(tblNhanVien.getValueAt(index, 8).toString());
        txtDiaChi.setText(tblNhanVien.getValueAt(index, 9).toString());
        boolean trangThai = tblNhanVien.getValueAt(index, 10).toString().equalsIgnoreCase("Còn làm việc") ? true : false;
        rdoDangLam.setSelected(trangThai);
        rdoNghiLam.setSelected(!trangThai);

    }

