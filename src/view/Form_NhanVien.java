    private NhanVien getNhanVienFrom() {
        NhanVien nv = new NhanVien();
    String maNv = txtMaNV.getText();
        String pass = txtMatKhau.getText();
        String ten = txtTenNv.getText();
        String soDt = txtSdt.getText();
        String ngaySinh = txtNgaySinh.getText();
        String diaChi = txtDiaChi.getText();
        String luongStr = txtLuong.getText();
        String email = txtEmail.getText();

    if (maNv.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Vui lòng nhập mã nhân viên!");
        return null;
    }
        if (pass.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập mật khẩu!");
            return null;
        }

        if (ten.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập tên nhân viên!");
            return null;
        }

        if (soDt.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập số điện thoại!");
            return null;
        }

        if (!soDt.matches("0[0-9]{9}")) {
            JOptionPane.showMessageDialog(this, "Nhập sai định dạng số điện thoại!");
            return null;
        }

        if (ngaySinh.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập ngày sinh!");
            return null;
        }

        if (!ngaySinh.matches("\\d{4}-\\d{2}-\\d{2}")) {
            JOptionPane.showMessageDialog(this, "Nhập sai định dạng ngày sinh!");
            return null;
        }

        if (diaChi.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập địa chỉ!");
            return null;
        }

        if (luongStr.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập lương!");
            return null;
        }

        try {
            Double luong = Double.parseDouble(luongStr);
            nv.setLuong(luong);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Nhập sai định dạng lương!");
            return null;
        }

        if (email.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập email!");
            return null;
        }

    nv.setMaNv(maNv);
        nv.setPass(pass);
        nv.setTen(ten);
        nv.setSdt(soDt);
        nv.setNgaySinh(convertStringToSqlDate(ngaySinh));
        nv.setDiaChi(diaChi);
        nv.setEmail(email);
        nv.setChucVu(rdoQuanLy.isSelected()?true:false);
        nv.setTrangThai(rdoDangLam.isSelected()?true:false);

        return nv;
    }

    private java.sql.Date convertStringToSqlDate(String dateStr) {
        SimpleDateFormat formatter = new SimpleDateFormat("yyyy-MM-dd"); // Định dạng ngày sinh của bạn
        try {
            Date utilDate = formatter.parse(dateStr); // Chuyển đổi thành java.util.Date
            return new java.sql.Date(utilDate.getTime()); // Chuyển đổi thành java.sql.Date
        } catch (ParseException e) {
            e.printStackTrace();
            return null; // hoặc có thể xử lý ngoại lệ theo cách khác
        }
    }

